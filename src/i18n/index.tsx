const messages: Record<AppLocale, Messages> = { 'zh-CN': baseZh, en: baseEn, de: baseDe };

const detectLocale = (): AppLocale => {
  const language = typeof navigator !== 'undefined' ? navigator.language.toLowerCase() : 'en';
  if (language.startsWith('de')) return 'de';
  if (language.startsWith('zh')) return 'zh-CN';
  return 'en';
};

const I18nContext = createContext<I18nContextValue>({
  locale: 'en',
  preference: 'system',
  t: (key) => key,
  formatDate: (value) => value.toISOString(),
  setPreference: () => undefined,
});

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [preference, setPreference] = useState<LocalePreference>(() => {
    if (typeof localStorage === 'undefined') return 'system';
    const value = localStorage.getItem(LOCALE_STORAGE_KEY);
    return value === 'system' || value === 'zh-CN' || value === 'en' || value === 'de' ? value : 'system';
  });

  const locale = preference === 'system' ? detectLocale() : preference;

  useEffect(() => {
    if (typeof document !== 'undefined') document.documentElement.lang = locale;
  }, [locale]);

  useEffect(() => {
    if (typeof localStorage !== 'undefined') localStorage.setItem(LOCALE_STORAGE_KEY, preference);
  }, [preference]);

  const value = useMemo<I18nContextValue>(() => ({
    locale,
    preference,
    t: (key, vars) => {
      const template = messages[locale][key] || messages.en[key] || key;
      return Object.entries(vars || {}).reduce((result, [varKey, varValue]) => (
        result.split(`{${varKey}}`).join(String(varValue))
      ), template);
    },
    formatDate: (value, options) => new Intl.DateTimeFormat(locale, options).format(value),
    setPreference,
  }), [locale, preference]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => useContext(I18nContext);
